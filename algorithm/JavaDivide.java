package algorithm;

public class JavaDivide {

    public static void main(String[] args) {
        for (int i =0 ; i < 11; i++) {
            System.out.print(i);
            System.out.print(":");
            System.out.print(i % 2);
            System.out.print(":");

            System.out.println(i / 2);
        }
    }
}
